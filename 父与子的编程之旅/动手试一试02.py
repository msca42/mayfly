# 创建一个变量，并赋给它一个数值，然后使用print函数显示
age = 18
print(age)

# 改变这个变量，用一个新值替换
age = 19
print(age)

# 创建另一个变量，并赋给它一个字符串，然后使用print函数显示
name = "mayfly"
print(name)

# 计算一周分钟
DaysPerWeek = 7
HoursPerDay = 24
MinutesPerHour = 60
print(DaysPerWeek * HoursPerDay * MinutesPerHour)

# 将一天改为26小时
HoursPerDay = 26
print(DaysPerWeek * HoursPerDay * MinutesPerHour)