#include <stdio.h>

int main(void)
{
  float x = 10;
  // a: 指数形式，栏宽8，左对齐，小数点后保留一位数字 
  printf("%-8.1e",x);
  // b: 指数形式，栏宽10，右对齐，小数点后保留6位数字
  printf("%10.6e",x);
  // c: 定点十进制，栏宽8，左对齐，小数点后保留3位数字
  printf("%-8.3f",x);
  // d: 定点十进制，栏宽6，右对齐，小数点后无数字
  printf("%6.f",x);
}
/*
// d: 定点十进制，栏宽6，右对齐，小数点后无数字
小数点后无数字，需要在栏宽后加上小数点否则，默认小数点后6位
*/