def printInfo(name, address, street, city, province, zip_code, country):
    print("name:", name)
    print("address:", address)
    print("street:", street)
    print("city:", city)
    print("province:", province)
    print("zip_code:", zip_code)
    print("country:", country)


printInfo("mayfly", "Carter Sande", "123 Main Street",
          "Ottawa", "Ontario", "K2M 2E9", "Canada")
