# 整数
# 运算 + 、- 、* 、/ 、** 、
num1 = 2
num2 = 3
print(num1 + num2)
print(num2 - num1)
print(num1 * num2)
print(num2 / num1)
print(num2 ** num1)

# 浮点数
# 注意事项：
  #  / 的结果是浮点数
  # 所有操作符带有浮点数的结果都是浮点数
num3 = 0.1
num4 = 0.2
print(num3 + num3)
print(num4 + num4)
print(2 * num3)
print(2 * num4)
print(num3 + num4)

