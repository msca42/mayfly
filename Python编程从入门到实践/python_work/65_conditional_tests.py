st1 = 'car'
st2 = 'car'

# 检查两个字符串是否相等和不等
print(st1 == st2)
print(st1 != st2)
# 使用lower()方法的条件测试
print(st1.lower() == st2)
# 数值比较
num1 = 10
num2 = 5
print(num1 == num2)
print(num1 != num2)
print(num1 > num2)
print(num1 < num2)
print(num1 >= num2)
print(num1 <= num2)
# 使用关键字and和or的条件测试
print(num1> num2 and num2< num1)
print(num1> num2 or num2< num1)
# 特定值是否在列表中
print('c' in st1)
print('c' not in st1)