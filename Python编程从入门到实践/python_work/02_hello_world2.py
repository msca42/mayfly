# 使用变量

# 变量名规则： 
  # 包含字母、数字和下划线，不能以数字开头
  # 不能包含空格，不能使用关键字和函数名作为变量名
  # 简短 描述性强
  # 不要使用字母l和o
  # 区分大小写，但是尽量使用小写，大写通常具有特殊含义

# 变量的本质
  # 通常描述为可以保存数据的存储单元
  # Python中常见说法 变量是可以被赋值的标签，也可以说变量指向特定的值

message = "Hello Python world!"
print(message)

message = "Hello Python Crash Course world!"
print(message)